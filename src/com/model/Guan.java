package com.model;

public class Guan {
	String number;
	String passed;
	String guan;
public  Guan(String number,String passed){
	this.number=number;
	this.passed=passed;
}
public Guan(){
	
}
public String getNumber() {
	return number;
}
public void setNumber(String number) {
	this.number = number;
}
public String getPassed() {
	return passed;
}
public void setPassed(String passed) {
	this.passed = passed;
}
public String getGuan() {
	return guan;
}
public void setGuan(String guan) {
	this.guan = guan;
}

}
